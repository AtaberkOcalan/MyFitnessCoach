    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        Accounts acn = Accounts.ActiveAccount();
        String ProjectName = tf_ProjectName.getText();
        DefaultTableModel model = (DefaultTableModel)New_Program.getModel();
        if (ProjectName.equals("")){
            JOptionPane.showMessageDialog(this, "Project name field cannot be empty.");
            return;
        }
        if (model.getRowCount() == 0){
            JOptionPane.showMessageDialog(this, "There is no movement in the table.");
            return;
        }
        for(int i = 0; i < ProjectName.length(); i++){
            if('/' == ProjectName.charAt(i)||',' == ProjectName.charAt(i)){
                JOptionPane.showMessageDialog(this, "The project name cannot contain , or /.");
                return;
            } 
        }
        try(Scanner scn = new Scanner(new BufferedReader(new FileReader("Programs.txt")))){
            while(scn.hasNextLine()){
                String s = scn.nextLine();
                String[] Array = s.split(",");
                if(Array[1].equals(ProjectName)&&Array[0].equals(acn.getUsername())){
                    JOptionPane.showMessageDialog(this, "You have a program with this name.");
                    return;
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        try(BufferedWriter writer = new BufferedWriter(new FileWriter("Programs.txt",true))){
            writer.write(acn.getUsername()+","+ProjectName + "," + "0" + "," + "0" + "/");
            for (int i = 0; i < New_Program.getRowCount(); i++){
                for(int j = 0; j < New_Program.getColumnCount(); j++){
                    if(j != New_Program.getColumnCount()-1){
                        writer.write((String)model.getValueAt(i, j)+",");
                    }
                    else{
                        writer.write((String)model.getValueAt(i, j));
                    }
                }
                if(i != New_Program.getRowCount()-1){
                    writer.write("/");
                }
                else{
                    writer.write("\n");
                }
                
            }
            
        } catch (IOException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Program saved successfully.");
    }//GEN-LAST:event_SaveButtonActionPerformed

