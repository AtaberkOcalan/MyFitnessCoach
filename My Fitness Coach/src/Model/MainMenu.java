    private void cb_MyProgramsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_MyProgramsActionPerformed
        DefaultTableModel model = (DefaultTableModel) SelectedProgram.getModel();
        Accounts acn = Accounts.ActiveAccount();
        while(SelectedProgram.getRowCount() != 0){
            model.removeRow(0);
        }
        for ( String program : RegisteredPrograms.Programs ){
            String[] find = program.split(",");
            if(find[1].equals(cb_MyPrograms.getSelectedItem())){
                MuscleActivation.reset();
                String[] Array1 = program.split("/");
                for ( int i = 1; i < Array1.length; i++ ){
                    String[] add = Array1[i].split(",");
                    model.addRow(add);
                    switch ( add[1]){
                        case "Back" -> { acn.setBackReps(Integer.valueOf(add[2])*Integer.valueOf(add[3]));
                        }
                        case "Chest" -> {acn.setChestReps(Integer.valueOf(add[2])*Integer.valueOf(add[3]));
                        }
                        case "Shoulders" -> {acn.setShouldersReps(Integer.valueOf(add[2])*Integer.valueOf(add[3]));
                        }
                        case "Leg" -> {acn.setLegReps(Integer.valueOf(add[2])*Integer.valueOf(add[3]));
                        }
                        case "Arm" -> {acn.setArmReps(Integer.valueOf(add[2])*Integer.valueOf(add[3]));
                        }
                        
                    }
                    
                }
                BackReps.setText(String.valueOf(acn.getBackReps()));
                ChestReps.setText(String.valueOf(acn.getChestReps()));
                ShouldersReps.setText(String.valueOf(acn.getShouldersReps()));
                LegReps.setText(String.valueOf(acn.getLegReps()));
                ArmReps.setText(String.valueOf(acn.getArmReps()));
                    
            }
        }
        
    }//GEN-LAST:event_cb_MyProgramsActionPerformed
    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        if(JOptionPane.showConfirmDialog(this, "Do you confirm that the program will be deleted?","Remove",
                JOptionPane.YES_NO_OPTION)==JOptionPane.YES_NO_OPTION){
            try(Scanner scn = new Scanner(new BufferedReader(new FileReader("Programs.txt")))){
                
                int deleteLine_number = 0;
                while ( scn.hasNextLine() ){
                    deleteLine_number++;
                    String ProgramInfo = scn.nextLine();
                    String[] Array = ProgramInfo.split(",");
                    if(Array[1].equals(cb_MyPrograms.getSelectedItem())){
                        cb_MyPrograms.removeItem(cb_MyPrograms.getSelectedItem());
                        cb_MyPrograms.setSelectedIndex(-1);
                        RegisteredPrograms.deleteLine(deleteLine_number);
                        RegisteredPrograms.Programs.remove(deleteLine_number-1);
                        RegisteredPrograms.ProgramsName.remove(deleteLine_number-1);
                        break;
                    }
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
            BackReps.setText("0");
            ChestReps.setText("0");
            ShouldersReps.setText("0");
            LegReps.setText("0");
            ArmReps.setText("0");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

